a "## 🔗 3. O Componente \`<Link>\`"
a ""
a "### O problema do \`<a href>\` tradicional"
a ""
a "A tag \`<a>\` recarrega o site INTEIRO a cada clique: descarta a página, pede tudo de novo ao servidor e remonta do zero (a tela pisca em branco). É lento."
a ""
a "### A solução: SPA + \`<Link>\`"
a ""
a "Apps Next são **SPAs (Single Page Applications)**: o site carrega uma vez e depois troca apenas o **pedaço** que muda. O \`<Link>\` dá esse comportamento à navegação."
a ""
a "\`\`\`jsx"
a "import Link from 'next/link';"
a ""
a "export default function Menu() {"
a "  return ("
a "    <nav style={{ display: 'flex', gap: '15px' }}>"
a "      <Link href=\"/\">Início</Link>"
a "      <Link href=\"/sobre\">Sobre</Link>"
a "      <Link href=\"/contato\">Contato</Link>"
a "    </nav>"
a "  );"
a "}"
a "\`\`\`"
a ""
a "### \`<a>\` vs \`<Link>\`"
a ""
a "| Critério | \`<a href>\` 🐢 | \`<Link href>\` 🚀 |"
a "|----------|--------------|------------------|"
a "| Recarrega a página? | Sim (pisca) | Não (instantâneo) |"
a "| Velocidade | Lenta | Rápida |"
a "| Mantém o estado do app? | ❌ Perde tudo | ✅ Mantém |"
a "| Pré-carrega a página (prefetch)? | ❌ Não | ✅ Sim |"
a ""
a "### 🔮 Prefetch"
a ""
a "Quando um \`<Link>\` aparece na tela, o Next já baixa a página de fundo. No clique, ela abre na hora. Por isso sites em Next são tão rápidos."
a ""
a "### Recursos extras"
a ""
a "\`\`\`jsx"
a "// Link dinâmico com variável"
a "const id = 42;"
a "<Link href={\`/produto/\${id}\`}>Ver produto</Link>"
a ""
a "// Desligar prefetch (casos específicos)"
a "<Link href=\"/sobre\" prefetch={false}>Sobre</Link>"
a "\`\`\`"
a ""
a "> 🎯 Regra: link **interno** (seu próprio site) → \`<Link>\`. Link **externo** (outro site) → \`<a>\`."
a ""
