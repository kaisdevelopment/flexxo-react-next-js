a "'use client';"
a ""
a "import { useState } from 'react';"
a ""
a "export default function ListaTarefas() {"
a "  const [tarefas, setTarefas] = useState(["
a "    { id: 1, texto: 'Estudar React 📚' },"
a "    { id: 2, texto: 'Tomar café ☕' },"
a "    { id: 3, texto: 'Dar aula 👨‍🏫' },"
a "  ]);"
a ""
a "  function adicionar() {"
a "    const nova = {"
a "      id: Date.now(),"
a "      texto: \`Tarefa \${tarefas.length + 1} ✨\`,"
a "    };"
a "    setTarefas([...tarefas, nova]);"
a "  }"
a ""
a "  return ("
a "    <div style={{ padding: '20px' }}>"
a "      <h2>📋 Minhas Tarefas ({tarefas.length})</h2>"
a "      <ul>"
a "        {tarefas.map((tarefa) => ("
a "          <li key={tarefa.id}>{tarefa.texto}</li>"
a "        ))}"
a "      </ul>"
a "      <button onClick={adicionar}>+ Adicionar tarefa</button>"
a "    </div>"
a "  );"
a "}"
a "\`\`\`"
a ""
a "> 💡 Dica: o par perfeito do \`.map()\` é o \`.filter()\` para remover itens:"
