a "## 📋 1. Renderizando Listas com \`.map()\`"
a ""
a "O \`.map()\` transforma **cada item de um array em um elemento JSX**."
a ""
a "\`\`\`jsx"
a "const frutas = ['🍎 Maçã', '🍌 Banana', '🍇 Uva'];"
a ""
a "{frutas.map((fruta) => <li>{fruta}</li>)}"
a "\`\`\`"
a ""
a "### 🔑 A \`key\` é obrigatória"
a ""
a "Cada item precisa de uma \`key\` única para o React identificá-lo."
a ""
a "\`\`\`jsx"
a "{frutas.map((fruta, index) => ("
a "  <li key={index}>{fruta}</li>"
a "))}"
a "\`\`\`"
a ""
a "> ⚠️ Prefira sempre um \`id\` real à \`key={index}\`, principalmente quando a lista pode mudar de ordem ou ter itens removidos."
